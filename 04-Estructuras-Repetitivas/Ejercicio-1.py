# 1) Crea un programa que imprima en pantalla todos los números enteros desde 0 hasta 100 (incluyendo ambos extremos), en orden creciente, mostrando un número por línea.

num = 0 # Inicializamos la variable 'num' con el valor 0

while num <= 100:  # Mientras 'num' sea menor o igual a 100, repetimos el bloque de código
    print(num) # Imprimimos el valor actual de 'num' en pantalla
    num = num + 1 # Incrementamos 'num' en 1 en cada iteración